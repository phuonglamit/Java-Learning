public class D02E02ArithmeticOperators {
    public static void main(String[] args) throws Exception {

        //Arithmetic Operators - Toan tu so hoc

        //Addition (+)
        int a = 5;
        int b = 7;
        int c = a + b;
        System.out.println("c =" +c);
        
        //Subtraction (-)
        int a1 = 5;
        int b1 = 3;
        int c1 = a1 - b1;
        System.out.println("c1 =" +c1);

        //Multiplication (*)
        int a2 = 2;
        int b2 = 4;
        int c2 = a2*b2;
        System.out.println("c2 =" +c2);

        //Devision ( / ) ==> int
        int a3 = 9;
        int b3 = 4;
        int c3 = a3/b3;
        System.out.println("c3 =" +c3);

        //Devision ( / ) ==> double
        int a4 = 9;
        int b4 = 4;
        double c4 = (double)a4/b4;
        System.out.println("c4 =" + c4);

        //Modulus (%) chia lay du

        int a5 = 9;
        int b5 = 4;
        double c5 = a5%b5;
        System.out.println("c5 =" + c5);

    }
    
}
