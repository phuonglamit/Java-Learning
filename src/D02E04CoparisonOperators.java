public class D02E04CoparisonOperators {
    public static void main(String[] args) {

        int a = 7;
        int b = 9;

        // Equal to (==):
        System.out.println(a == b);

        // Not equal to (!=):
        System.out.println(a != b);

        // Greater than (>):
        System.out.println(a > b);

        // Less than (<)
        System.out.println(a < b);

        // Greater than or equal to (<=)

        System.out.println(a >= b);
        // Less than or equal to (<=)
        System.out.println(a <= b);
    }
}
